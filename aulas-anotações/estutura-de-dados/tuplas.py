# Tuplas são estrutturas parecidas com listas
# Tuplas são imutáveis, listas mutáveis

# Criação
frutas = ("laranja","pera","uva",) # usar () e normalmente colocar ',' no final
print(frutas)

letras = tuple("python")
print(letras)

numeros = tuple([1, 2, 3, 4])
print(numeros)

pais = ("Brasil",)
print(pais)

# Os métodos da tupla são iguais aos da lista